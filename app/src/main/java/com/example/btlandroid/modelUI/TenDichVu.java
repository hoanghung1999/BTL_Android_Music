package com.example.btlandroid.modelUI;

public class TenDichVu {
    private int img;
    private String ten;

    public TenDichVu() {
    }

    public TenDichVu(int img, String ten) {
        this.img = img;
        this.ten = ten;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
